    TEAM_ID integer not null,
    MYTEAM_ID integer not null,
    SAVE integer,
    LOSE integer,
    WIN integer,
    SHUTOUT integer not null,
    COMPLETE integer not null,
    RUNS integer not null,
    STRIKE_OUT integer not null,
    FOUR_BALL integer not null,
    HOMERUN integer not null,
    HIT integer not null,
    PA integer not null,
    INNING double not null,
    GAME_ID integer not null,
    PLAYER_ID integer not null,
