create table GAME (
    GAME_ID integer generated by default as identity,
    BOTTOM_5th integer,
    TOP_5th integer,
    BOTTOM_4th integer,
    TOP_4th integer,
    BOTTOM_3rd integer,
    TOP_3rd integer,
    BOTTOM_2nd integer,
    TOP_2nd integer,
    BOTTOM_1st integer,
    TOP_1st integer,
    LAST_RUN integer not null,
    FIRST_RUN integer not null,
    LAST_TEAM integer not null,
    FIRST_TEAM integer not null,
    GAME_NUMBER integer not null,
    GAME_DATE date not null,
    constraint GAME_PK primary key(GAME_ID)
);
